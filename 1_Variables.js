
//Teach me about variables in Javascript

/*Variables in Javascript are containers that store data values.*/
/*they are declared using var, let or const keyword*/
/*var is function scoped, let and const are block scoped*/
/*let and const are introduced in ES6*/

var name = "John";
let age = 25;
const country = "India";

// console.log(name);
// console.log(age);
// console.log(country);

/*Variables can be declared without assigning any value*/

let x;
x=10;
// console.log(x);

/*Variables can be reassigned*/

let y = 5;
y = 10;
// console.log(y);

/*Variables can be reassigned with different data types*/

let z = 5;
z = "Hello";
// console.log(z);

/*Variables can be declared and assigned in one line*/

let a = 10;
// console.log(a);

/*Variables can be declared and assigned in multiple lines*/

let b;
b = 20;
// console.log(b);

/*Variables can be declared and assigned in one line and reassigned in multiple lines*/

let c = 30;
c = 40;
// console.log(c);

/*Variables can be declared and assigned in multiple lines and reassigned in one line*/

let d;
d = 50;
// console.log(d);


